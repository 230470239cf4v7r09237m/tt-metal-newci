class Conv:
    def __init__(
        self,
        device,
        parameter,
        conv_pt,
        enable_act=True,
        is_detect=False,
        enable_identity=False,
        is_dfl=False,
        config_override=None,
        use_1d_systolic_array=True,
    ):
        self.enable_identity = enable_identity
        self.enable_act = enable_act
        print("use_1d_systolic_arrayuse_1d_systolic_array", use_1d_systolic_array)
        self.conv = Yolov10_Conv2D(
            parameter.conv,
            conv_pt.conv,
            device=device,
            is_detect=is_detect,
            is_dfl=is_dfl,
            config_override=config_override,
            use_1d_systolic_array=use_1d_systolic_array,
        )

    def __call__(self, x):
        x = self.conv(x)

        if self.enable_act:
            if x.is_sharded():
                x = ttnn.sharded_to_interleaved(x, ttnn.L1_MEMORY_CONFIG)
            if self.enable_identity:
                x = ttnn.identity(x, memory_config=ttnn.L1_MEMORY_CONFIG)
            else:
                x = ttnn.silu(x)

        return x
